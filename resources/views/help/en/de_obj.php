Trouve le complément (du verbe, de l'adjectif ou de l'adverbe) introduit par <em>de</em>, si le <em>de</em> est présent, il faut le sélectionner, s'il est absent, il faut sélectionner ce qui le remplace (en général <em>en</em> ou <em>dont</em>) :
<em>
  <ul>
	<li>&nbsp;•&nbsp;… soupçonné d'avoir <span class=highlight>bénéficié</span> <span class=solution>de</span> fausses factures…</li>
	<li>&nbsp;•&nbsp;… <span class=highlight>passionné</span> <span class=solution>de</span> météorologie…</li>
	<li>&nbsp;•&nbsp;… ont été recueillies <span class=highlight>séparément</span> <span class=solution>de</span> celles concernant les événements indésirables.</li>
	<li>&nbsp;•&nbsp;Ils ont étudié les dialogues du livre et <span class=solution>en</span> ont <span class=highlight>extrait</span> quelques-uns.</li>
	<li>&nbsp;•&nbsp;Une entreprise <span class=solution>dont</span> on <span class=highlight>reparlera…</span></li>
  </ul>
Parfois c'est un <em>que</em> qui peut être l'object indirect recherché (il est alors équivalent à <em>de ce que</em>) :
  <ul>
	<li>&nbsp;•&nbsp;<span class=highlight>informez</span> votre dentiste <span class=solution>que</span> vous allez être traîter par Aclasta.</li>
  </ul>
</em>
