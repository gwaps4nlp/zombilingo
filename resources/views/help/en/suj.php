	<li>&nbsp;•&nbsp;Une première <span class=solution>réunion</span> a <span class=highlight>eu</span> lieu sur la question de l’indemnisation.</li>
	<li>&nbsp;•&nbsp;Aucune <span class=solution>adaptation</span> de la dose n’<span class=highlight>est</span> nécessaire</li>
	<li>&nbsp;•&nbsp;…le <span class=solution>maire</span> el la directrice ont <span class=highlight>conduit</span> la visite commentée des locaux.</li>
	<li>&nbsp;•&nbsp;…une forge <span class=solution>qui</span> <span class=highlight>connut</span> son apogée au XIXe siècle.</li>
  </ul>
</em>

<br/>
<b>Note</b>&nbsp;: pour une personne, le mot principal est 1- son titre (<em>Monsieur</em>, <em>M.</em>, <em>Président</em>, etc) 2- son prénom (et non son nom)
