Trouve le complément (du verbe, de l'adjectif ou de l'adverbe) introduit par <em>de</em>, si le <em>de</em> est présent, il faut le sélectionner, s'il est absent, il faut sélectionner ce qui le remplace (en général <em>en</em> ou <em>dont</em>)&#8239;:
<em>
  <ul>
	<li><span class="sentence">… soupçonné d'avoir <span class=highlight>bénéficié</span> <span class=solution>de</span> fausses factures…</span></li>
	<li><span class="sentence">… <span class=highlight>passionné</span> <span class=solution>de</span> météorologie…</span></li>
	<li><span class="sentence">… ont été recueillies <span class=highlight>séparément</span> <span class=solution>de</span> celles concernant les événements indésirables.</span></li>
	<li><span class="sentence">Ils ont étudié les dialogues du livre et <span class=solution>en</span> ont <span class=highlight>extrait</span> quelques-uns.</span></li>
	<li><span class="sentence">Une entreprise <span class=solution>dont</span> on <span class=highlight>reparlera…</span></span></li>
  </ul>
</em>
Parfois c'est un <em>que</em> qui peut être l'object indirect recherché (il est alors équivalent à <em>de ce que</em>)&#8239;:
<em>
  <ul>
	<li><span class="sentence"><span class=highlight>informez</span> votre dentiste <span class=solution>que</span> vous allez être traîté par Aclasta.</span></li>
  </ul>
</em>
Certains compléments à l'infinitif sont introduits par une préposition <em>de</em> mais sont des objets&#8239;!
Dans ce cas, il faut cliquer sur la croix d'os.
Pour les détecter, pense à reformuler comme une question&#8239;; quelques exemples&#8239;:
  <ul>
	<li><em><span class="sentence">Il <span class=highlight>convient</span> <span class=not_solution>de</span> modifier le régime.</em> (On peut dire&#8239;: <em>Que convient-il de modifier&#8239;?</em> mais pas&#8239;: <em>De quoi convient-il de modifier&#8239;?</em>)</span></li>
	<li><em><span class="sentence">Or elle aurait <span class=highlight>décidé</span> <span class=not_solution>de</span> jeter l'éponge.</em> (On peut dire&#8239;: <em>Qu'a-t-elle décidé&#8239;?</em> mais pas&#8239;: <em>De quoi a-t-elle décidé&#8239;?</em>)</span></li>
  </ul>
