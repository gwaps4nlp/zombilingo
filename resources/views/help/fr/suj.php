Trouve le sujet du verbe surligné. Si le sujet est un groupe de mots (groupe nominal), c'est le mot principal qu'il faut sélectionner.
<em>
  <ul>
    <li><span class="sentence">Une première <span class=solution>réunion</span> a <span class=highlight>eu</span> lieu sur la question de l’indemnisation.</span></span></li>
    <li><span class="sentence">Aucune <span class=solution>adaptation</span> de la dose n’<span class=highlight>est</span> nécessaire.</span></span></li>
  </ul>
</em>

Si le sujet contient une coordination (avec un <em>et</em> ou un <em>ou</em>), sélectionne le premier élément.
<em>
  <ul>
    <li><span class="sentence">…le <span class=solution>maire</span> el la directrice ont <span class=highlight>conduit</span> la visite commentée des locaux.</span></span></li>
  </ul>
</em>

Si le verbe est dans une proposition relative introduite par <em>qui</em>, le sujet est le pronom relatif <em>qui</em>.
<em>
  <ul>
    <li><span class="sentence">…une forge <span class=solution>qui</span> <span class=highlight>connut</span> son apogée au XIXe siècle.</span></span></li>
  </ul>
</em>

<b>Note</b>&nbsp;: pour une personne, le mot principal est son titre (<em>Monsieur</em>, <em>M.</em>, <em>Président</em>, etc) s'il y en a un. Sinon, c'est le prénom (et non son nom)
