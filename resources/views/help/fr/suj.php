Trouve le sujet du verbe surligné. Si le sujet est un groupe de mots (groupe nominal), c'est le mot principal qu'il faut sélectionner.
<em>
  <ul>
    <li>&nbsp;•&nbsp;Une première <span class=solution>réunion</span> a <span class=highlight>eu</span> lieu sur la question de l’indemnisation.</li>
    <li>&nbsp;•&nbsp;Aucune <span class=solution>adaptation</span> de la dose n’<span class=highlight>est</span> nécessaire</li>
    <li>&nbsp;•&nbsp;…le <span class=solution>maire</span> el la directrice ont <span class=highlight>conduit</span> la visite commentée des locaux.</li>
    <li>&nbsp;•&nbsp;…une forge <span class=solution>qui</span> <span class=highlight>connut</span> son apogée au XIXe siècle.</li>
  </ul>
</em>

Si le sujet contient une coordination (avec un <em>et</em> ou un <em>ou</em>), sélectionne le premier élément.
<em>
  <ul>
    <li>&nbsp;•&nbsp;…le <span class=solution>maire</span> el la directrice ont <span class=highlight>conduit</span> la visite commentée des locaux.</li>
  </ul>
</em>

Si le verbe est dans une proposition relative introduite par qui, le sujet est le pronom relatif <em>qui</em>.
<em>
  <ul>
    <li>&nbsp;•&nbsp;…une forge <span class=solution>qui</span> <span class=highlight>connut</span> son apogée au XIXe siècle.</li>
  </ul>
</em>

<b>Note</b>&nbsp;: pour une personne, le mot principal est son titre (<em>Monsieur</em>, <em>M.</em>, <em>Président</em>, etc) s'il y en a un. Sinon, c'est le prénom (et non son nom)
