<p>Les auxiliaires</span> sont des verbes utilisés pour former un temps composé lorsqu‘ils sont associés à des <b>verbes</b> au participe passé. Le français utilise deux auxiliaires: <em>être et avoir</em>. Nous ne considérons pas les modaux (pouvoir, devoir, etc) comme des auxiliaires :
<em>
  <ul>
    <li><span class="sentence">Max <span class="solution">a</span> <span class="highlight">marché</span> pendant des heures.</span></li>
    <li><span class="sentence">Il <span class="solution">était</span> <span class="highlight">tenu</span> au secret.</span></li>
  </ul>
</em>
</p>

<p>Attention, dans certains cas, l'auxiliaire est un passif et ne doit pas être annoté comme un auxiliaire simple&nbsp;:

<em>
  <ul>
    <li><span class="sentence">Elle était <span class="highlight">admirée</span> de tous</span></li>
  </ul>
</em>
</p>

<p>Le verbe associé à l‘auxiliaire peut lui aussi être être ou avoir&nbsp;:
<em>
  <ul>
    <li><span class="sentence">Il <span class="solution">a</span> <span class="highlight">eu</span> alors comme élèves&nbsp;: les israéliens Galit Chait/Sergei Sakhnovski, les britanniques Sinead Kerr/John Kerr…</span></li>
  </ul>
</em>
</p>
<p>Dans certains cas (passif), on peut avoir les verbes avoir et être qui se suivent. Il faut alors toujours sélectionner avoir&nbsp;:
<em>
  <ul>
    <li><span class="sentence">Josiane Bernard <span class="solution">a</span> été <span class="highlight">élue</span>  en 2001 conseillère générale du canton de Bagnolet et vice-présidente du Conseil général de Seine-Saint-Denis.</span></li>
  </ul>
</em>
</p>


