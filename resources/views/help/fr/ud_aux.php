<p>Le relation d’auxiliaire désigne le lien qui existe entre un auxiliaire et le verbe qui lui est associé.</p>

<p>En français, deux auxiliaires sont possibles&nbsp;: <em>être</em> et <em>avoir</em>.</p>

<em>
  <ul>
    <li><span class="sentence">Max <span class=solution>a</span> <span class=highlight>marché</span> pendant des heures</span></li>
    <li><span class="sentence">Il <span class=solution>est</span> <span class=highlight>venu</span> cherché son sac</span></li>
  </ul>
</em>

<p>Le <b>verbe</b> associé à l'auxiliaire peut lui aussi être <em>être</em> ou <em>avoir</em>&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">Quelles conséquences cela <span class=solution>a</span>-t-il <span class=highlight>eu</span>&nbsp;?</span></li>
    <li><span class="sentence">et euh je pense que ça <span class=solution>a</span> <span class=highlight>été</span> très bénéfique </span></li>
  </ul>
</em>

<p>Attention, il est possible de trouver deux auxiliaires en même temps… dans l'exemple suivant, c'est bien <em>a</em> qu'il faut sélectionner car dans ce cas, <em>été</em> est un auxiliaire passif.</p>

<em>
  <ul>
    <li>
      <span class="sentence">Le film <span class=solution>a</span> été <span class=highlight>tourné</span> à Los Angeles</span>
    </li>
  </ul>
</em>
