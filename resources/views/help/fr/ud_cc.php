<p>La conjonction de coordination relie deux éléments de la phrase. Cette conjonction peut être simple (<em>et</em>, <em>ou</em>, <em>mais</em>, <em>ni</em>…) ou composée (<em>parce que</em>, <em>à moins que</em>, <em>c'est-à-dire</em>…). Ici, il faut trouver l'élément coordonné à droite de la conjonction&nbsp;:</p>
<em>
  <ul>
    <li><span class="sentence">Les plats sont copieux <span class="highlight">et</span> <span class="solution">cuisinés</span> avec de bons produits.</span></li>
    <li><span class="sentence">Nous avons attendu sept <span class="highlight">ou</span> <span class="solution">huit</span> heures dans la gare.</span></li>
    <li><span class="sentence">La fille du cousin du père de Zoé, <span class="highlight">c’est-à-dire</span> la <span class="solution">cousine</span> de Zoé, est une astronaute.</span></li>
  </ul>
</em>

<p>La conjonction de coordination peut être en début de phrase&nbsp;:</p>
<em>
  <ul>
    <li><span class="highlight">Et</span> l'acte que j'accomplis ici n'est qu'un <span class="solution">moyen</span> révolutionnaire…</li>
  </ul>
</em>
