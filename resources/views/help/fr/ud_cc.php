<p>La conjonction de coordination relie deux éléments de la phrase. Cette conjonction peut être simple (mais, ou, et, donc, or, ni, car) ou composée (parce que, à moins que, c'est-à-dire, etc.). Ici, il faut trouver l'élément coordonné à droite de la conjonction :</p>

<em>
  <ul>
    <li><span class="sentence">Les plats sont copieux <span class="highlight">et</span> <span class="solution">cuisinés</span> avec de bons produits.</span></li>
    <li><span class="sentence"> Nous avons attendu sept <span class="highlight">ou</span> <span class="solution">huit</span> heures dans la gare.</span></li>
  </ul>
</em>
</p>
<p>Attention, on peut aussi utiliser des conjonctions de coordination pour expliquer («&nbsp;c’est-à-dire&nbsp;»)&nbsp;:
<em>
  <ul>
    <li><span class="sentence">La fille du cousin du père de Zoé, <span class="highlight">c’est-à-dire</span> la <span class="solution">cousine</span> de Zoé, est une astronaute</span>.</li>
  </ul>
</em>
</p>
<p>Pour nuancer («&nbsp;Mais&nbsp;»)&nbsp;:
<em>
  <ul>
    <li><span class="sentence"><span class="highlight">Mais</span> faut aussi être <span class="solution">réaliste</span>.</span></li>
  </ul>
</em>
</p>
<p>Pour exclure une possibilité («&nbsp;Ni&nbsp;»)&nbsp;:
<em>
  <ul>
    <li><span class="sentence"><span class="highlight">Ni</span> <span class="solution">toi</span> ni lui n’êtes arrivés au bout de la course.</span></li>
  </ul>
</em>
</p>
