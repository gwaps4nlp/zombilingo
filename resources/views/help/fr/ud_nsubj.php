<p>La relation sujet nominal désigne le lien entre le sujet et un mot de la phrase.
Les sujets peuvent avoir différentes «&nbsp;natures&nbsp;» et les mots dont ils dépendent également.</p>


<p>Le sujet peut être un pronom&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence"><span class=solution>Je</span> <span class=highlight>peux</span> pas vous dire.</span></li>
  </ul>
</em>

<p>ou bien le sujet peut aussi être un nom commun&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">Le plus jeune <span class=solution>participant</span> a <span class=highlight>gagné</span> la course.</span></li>

  </ul>
</em>

<p>on peut aussi rencontrer une coordination&nbsp;; dans ce cas, le mot à chercher est toujours celui qui apparaît <b>avant</b> la conjonction de coordination&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">j’ai trouvé que l’<span class="solution">enseignement</span> et la recherche <span class=highlight>allaient</span> bien ensemble généralement.</span></li>
  </ul>
</em>

<p>le sujet peut être un nom propre&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence"><span class=solution>Jésus</span> ne <span class=highlight>dit</span> rien avant la fin de l'épisode</span></li>
  </ul>
</em>

<p>dans le cas des noms propres dont on aurait le prénom et le nom, il faut sélectionner le prénom de la personne qui est sujet&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence"><span class=solution>Phillipe</span> Zdar est  <span class=highlight>tombé</span> depuis la fenêtre de son immeuble hier.</span></li>
  </ul>
</em>

<p>le sujet peut également être une nom de fonction&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">Le <span class=solution>coach</span> Debord a <span class=highlight>dit</span> que c’était plus possible de venir au match sans protège-tibias.</span></li>
  </ul>
</em>

<p>Le mot surligné n'est pas obligatoirement un verbe, les sujets peuvent également dépendre de mots qui sont des adjectifs ou des noms&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">L'<span class=solution>art</span> est le <span class=highlight>reflet</span> de la société.</span></li>
    <li><span class="sentence">enfin toute ma <span class=solution>famille</span> est <span class=highlight>portugaise.</span></li>
  </ul>
</em>

