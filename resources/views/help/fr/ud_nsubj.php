<p>La relation de sujet nominal dénote la dépendance des sujets nominaux d’une proposition énonciative à l’argument principale de la proposition.</p>


<p>L’argument peut être de nature verbale&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">Le plus jeune <span class=solution>participant</span> a <span class=highlight>gagné</span> la course.
  </ul>
</em>

<p>nominale&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">L'<span class=solution>art</span> est le <span class=highlight>reflet</span> de la société.
  </ul>
</em>

<p>ou advectivale&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence">enfin toute ma <span class=solution>famille</span> est <span class=highlight>portugaise</span>.
  </ul>
</em>

<p>De plus le sujet nominal a retrouvé peut également correspondre à un pronom&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence"><span class=solution>Vous</span> allez être <span class=highlight>médecin</span> en France en hôpital.
  </ul>
</em>

<p>ou bien à un nom propre&nbsp;:</p>

<em>
  <ul>
    <li><span class="sentence"><span class=solution>Jésus</span> ne <span class=highlight>dit</span> rien avant la fin de l’épisode.
  </ul>
</em>

<p>Il ne s’agit pas automatiquement d’un nom commun.</p>
