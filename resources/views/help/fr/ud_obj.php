<p>Un objet direct est l'objet du verbe : 
<em>
  <ul>
    <li><span class="sentence">on <span class="highlight">voit</span> une jeune <span class="solution">fille</span> habillée en noir.</span></li>
  </ul>
</em>
</p>
<p>Il peut également être un pronom réfléchi dans un verbe pronominal lorsque le pronom est un objet direct du verbe :
<em>
  <ul>
    <li><span class="sentence">il <span class="solution">se</span> <span class="highlight">lave</span>.</span></li>
  </ul>
</em>
</p>

<p>Lorsque le pronom fait partie d'une expression verbale fixe, il est également considéré en tant qu'objet direct (<span class="sentence"><span class="solution">se</span> <span class="highlight">douter</span>, <span class="solution">se</span> <span class="solution">souvenir</span>, etc.).</span></p>

<p>En cas de doute, on peut utiliser un test d’interrogation : on pose une question de manière à trouver ce qui est affecté par le verbe. Dans l'exemple ci-dessous, «&nbsp;qu'est-ce qui a été repris&nbsp;» ?
<em>
  <ul>
    <li><span class="sentence">En 1872, quand les Georgiens ont <span class="highlight">repris</span> les <span class="solution">commandes</span> du gouvernement, Barnett (qui avait été réélu par ce point) a rapporté le sceau.</span></li>
  </ul>
</em>
</p>


