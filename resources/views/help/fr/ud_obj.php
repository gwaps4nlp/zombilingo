<p>Un complément est un mot qui est rattaché directement au verbe de la phrase. Ce complément comme son nom l’indique complète la phrase. C'est ce qu'on appelle habituellement le COD (complément d'objet direct).</p>

<em>
  <ul>
    <li>
      <span class="sentence">
        il <span class=highlight>propose</span> une <span class=solution>série</span> d'activitées
      </span>
    </li>
    <li>
      <span class="sentence">
        Back <span class=highlight>frappe</span> alors le <span class=solution>ballon</span>
      </span>
    </li>
  </ul>
</em>

<p>Comme pour un sujet, si le complément est une coordianation de plusieus éléments, il faut sélectionner le premier.</p>
<em>
  <ul>
    <li>
      <span class="sentence">
        il <span class=highlight>étudie</span> la <span class=solution>philosophie</span> et la <span class=not-solution>théologie</span>
      </span>
    </li>
  </ul>
</em>

