<h2>Charter of admission to ZombiLingo</h2>

<p>
ZombiLingo is a free game created as part of a research project by <a href="http://www.schplaf.org/kf/index_en.php" target="_blank">Karën Fort</a> (associate prof. at Univ. Paris-Sorbonne)
and <a href="http://www.loria.fr/~guillaum" target="_blank">Bruno Guillaume</a> (researcher at Inria Grand Est),
with Nicolas Lefebvre (Engineer at Inria Grand Est).
The aim of the project is to build language resources (lexicons and annotated corpora) for Natural Language Processing (NLP) applications.
The language data is produced by the players' activity on ZombiLingo.
</p>
<p>
The produced data is freely {!! link_to('information','available',['target'=>'_blank']) !!} under various CC licences (depending on the licence of the original corpus), which are specified in each resource.
For the time being, the code of ZombiLingo itself remains private and belongs in its entirety to its authors.
Any access, usage, misappropriation, publication or disclosure of information of parts or whole of this code without the prior written consent from its authors will be subject to legal proceedings.
</p>
<p>
The present charter aims to allow players to participate in an enjoyable environment, provided they respect the rules detailed below.
</p>
<p>
Any breach or abuse of the rules will lead to penalties, including the permanent banishment of the faulty player.
Players are expected to be respectful of others and to allow everyone to enjoy the game.
</p>
<h2>
(1) Registration
</h2>
<p>
ZombiLingo is a free game and registration is open to anybody provided they have internet access.
Messages can be sent among players if the evolution of the game will include this feature.
The corpora dealt with in ZombiLingo may contain texts that are not suitable for persons under 16.
</p>
<h2>
(2) Personal Data
</h2>
<p>
We only collect information for the game to function properly. This information is automatically processed. Some of it is personal (password, Email addresses, etc), others (answers given during the game, ranking, etc.) may be shown to other people within the game or for research purposes.
We will NOT distribute or sell any personal data from the players outside the game itself (information presented in the leaderboard, display of the results of one game, etc).
s</p>
<p>
All the data is accessible by all the members of the development team without restriction.
The team will use the collected language data for research purposes.
This data does not contain personal information that can be traced back to the author. 
</p>
<p>
Players can delete their account at anytime, with all the information contained in the database. However, the data created by the player and his or her actions in the game will be kept if they are useful for the language resource.
</p>
<p>
