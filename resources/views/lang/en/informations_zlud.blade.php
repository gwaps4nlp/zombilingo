<h1 class="text-center">About ZombiLUDik</h1>

<h2>What is ZombiLUDik?</h2>
<p>
ZombiLUDik is a spinoff of ZombiLingo, a Game With A Purpose which aims to annotate corpora with dependency syntax relations as well as to gather other language data.
Points (brains) are won when annotating sentences, and annotating them well will help you gain even more. Conversely, bad annotations will generate a waste of time and points.
</p>

<p>
ZombiLUDik offers other ways to win points that you will discover while playing.
This game is intended for people interested in language and grammar, as well as zombie addicts.
</p>

