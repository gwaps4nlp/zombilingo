<h2>Charte d'Admission au jeu ZombiLingo</h2>

<p>
ZombiLingo est un jeu gratuit créé dans le cadre d'un projet de recherche
par <a href="http://www.schplaf.org/kf/" target="_blank">Karën Fort</a>
et <a href="http://www.loria.fr/~guillaum" target="_blank">Bruno Guillaume</a>
au sein du <a href="http://www.loria.fr" target="_blank">LORIA</a>,
avec l'aide d'Hadrien Chastant et de Valentin Stern (alors étudiants à l'Université de Lorraine).
L'objet de ce projet est la construction de ressources lexicales et de corpus annotés pour des applications de traitement automatique de la langue (TAL).
Ces données sont le produit de l'activité des joueurs de ZombiLingo.
</p>
<p>
Les données sont {!! link_to('informations','accessibles',['target'=>'_blank']) !!} sous des licences libres variées (fonction de la licence de la ressource d'origine), qui sont précisées dans chaque ressource.
Le code source de ZombiLingo reste, lui, pour l'instant privateur et appartient en totalité à ses auteurs.
Tout accès, utilisation, détournement, publication ou divulgation d'informations de tout ou partie de ce code sans le consentement écrit des auteurs sera passible de poursuites.
</p>
<p>
La présente charte a pour but de permettre aux joueurs de jouer dans l'environnement le plus agréable possible, s'ils respectent les quelques règles établies ci-dessous.
</p>
<p>
Tout manquement ou tout abus fera l'objet de sanctions pouvant aller jusqu'au banissement définitif du joueur.
De manière générale, il est attendu de la part des joueurs un comportement fair-play visant à respecter l'amusement de tous.
</p>
<h2>
(1) Inscription
</h2>
<p>
La participation au jeu est entièrement gratuite et l'inscription est ouverte à tous les internautes disposant d'un accès à Internet régulier et éventuellement d'une adresse mail personnelle et active (ce dernier point n'étant pas obligatoire).
Des mails pourront être envoyés au joueur en fonctions des évolutions du jeu.
Les corpus proposés dans ZombiLingo peuvent contenir des textes ne convenant pas à des personnes de moins de 16 ans.
</p>
<h2>
(2) Données Personnelles
</h2>
<p>
Les informations recueillies font l’objet d’un traitement informatique destiné à assurer le bon fonctionnement du jeu.
Certaines informations enregistrées dans notre base de données sont personnelles (mot de passe, adresse mail, etc.), d'autres sont amenées à être éventuellement fournies à d'autres personnes (réponses proposées par le joueur lors d'une partie, classement, etc.) dans le cadre du jeu.
Aucune diffusion des Données Personnelles ne sera faite à aucun titre que ce soit en dehors des modalités prévues par le jeu (informations affichées dans le Classement, affichage du résultat d'une partie, etc.).
</p>
<p>
Les destinataires des données sont&nbsp;: tous les membres de l'équipe du jeu, sans restriction.
L'équipe du jeu se réserve le droit d'exploiter à des fins de recherches les données langagières récoltées lors d'une partie (une fois celle-ci achevée).
En aucun cas, ces données ne contiennent d'informations personnelles permettant de retrouver leur(s) auteur(s).
</p>
<p>
Le joueur peut à tout moment supprimer son compte ainsi que la totalité des informations le concernant dans la base de données.
Cependant, les actions de jeu liées à ce joueur et qui sont utiles pour le construction de la ressource linguistiques seront conservées.
</p>
