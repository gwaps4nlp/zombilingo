<h1 class="text-center">À propos de ZombiLingo</h1>

<h2>Quoi ?</h2>
<p>
ZombiLingo est un jeu dont le but est d'annoter des corpus en syntaxe de dépendances et de récolter d'autres données langagières.
Les points (des cerveaux) peuvent être gagnés non seulement en annotant beaucoup de phrases, mais surtout en les annotant correctement.
</p>

<p>
ZombiLingo propose également d'autres moyens de gagner des points, qui sont laissés à la découverte du joueur.
Ce jeu s'adresse aux amoureux de la langue et de la grammaire, ainsi qu'aux fanatiques du monde des Zombies.
</p>

<p>
Avant de jouer, lisez la {!! link_to('charter','charte') !!} de ZombiLingo.
</p>


